# 1 - Crie uma função que calcule a área de um quadrado, de um retângulo e de um triângulo
def calcular_area_de_um_cuadrado(num1):
    areaCuadrado = num1 ** 2
    return areaCuadrado

def calcular_area_de_um_retangulo(num1, num2):
    areaRetangulo = num1 * num2
    return areaRetangulo

def calcular_area_de_um_triangulo(num1, num2):
    areaTriangulo = (num1 * num2) / 2
    return areaTriangulo